<?php
namespace PhpUtils;

class RandString
{

    static public function getRandString($length)
    {
        $str = null;
        $strPol = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789abcdefghijklmnopqrstuvwxyz";
        $max = strlen($strPol) - 1;
        for ($i = 0; $i < $length; $i ++) {
            $str .= $strPol[mt_rand(0, $max)];
        }
        return $str;
    }
}

